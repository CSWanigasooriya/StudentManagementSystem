    private boolean checkUserName(){
        try{
            String sql="Select count(*) from student where userName='"+user+"'";
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            rs.next();
                if(rs.getInt(1)>1){
                JOptionPane.showMessageDialog(this, "Username already in use");
                return false;
            }
        }
        catch(Exception ex){
            System.out.println(ex);
        }
        finally{
            try{
                rs.close();
                pst.close();
            }
            catch(Exception ex){
                System.out.println(ex);
            }
        }
        return true;
    }
