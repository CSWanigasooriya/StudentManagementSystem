    private void display(){
        try{
            pst=con.prepareStatement("select * from student where userName='"+user+"' or email='"+user+"'");
            rs=pst.executeQuery();
            while(rs.next()){
                if("qwerty123".equals(rs.getString(10))){
                    JOptionPane.showMessageDialog(this, "Please change your default password in your Profile");
                }
            profileId.setText((rs.getString(1)));
            userPass.setText((rs.getString(10)));
            profileUsr.setText(rs.getString(9));
            fName.setText((rs.getString(2)));
            lName.setText((rs.getString(3)));
            nic.setText((rs.getString(4)));
            mail.setText((rs.getString(6)));
            phone.setText((rs.getString(7)));
            ((JTextField) profileDate.getDateEditor().getUiComponent()).setText(rs.getString(5));
            while(rs.getBytes(11)!=null){
            byte[] img = rs.getBytes(11);
            ImageIcon image = new ImageIcon(img);
            Image im = image.getImage();
            Image myImg = im.getScaledInstance(imgLabel.getWidth(), imgLabel.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon newImage = new ImageIcon(myImg);
            imgLabel.setIcon(newImage);
            }
            }
        }
        catch(Exception ex){
            System.out.println(ex);
        }
        
        
    }
