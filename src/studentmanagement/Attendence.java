private void populateTable() {
        try {
            String adm = "Select cid,SUM(slot1+slot2) from attendence group by cid";
            pst = con.prepareStatement(adm);
            rs = pst.executeQuery();
            tm = (DefaultTableModel) userAttendenceTable.getModel();
            tm.setRowCount(0);
            while (rs.next()) {
                Object o[] = {rs.getString(1), rs.getInt(2)};
                tm.addRow(o);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
    }
